#napraviti program u kome se ucitava temperatura u celzijusima i ispisuje se temperatura u farenhajtima

c=float(input("Unesite vrijednost temperature u Celzijusima: ")) #ucitavanje temperature u celzijusima
f = c * 9/5 + 32 #pretvaranje temperature iz celzijusa u farenhajte koristeci formulu F = C * 9/5 + 32
print("Temperatura u Farenhajtima je:", f) #ispisivanje temperature u farenhajtima